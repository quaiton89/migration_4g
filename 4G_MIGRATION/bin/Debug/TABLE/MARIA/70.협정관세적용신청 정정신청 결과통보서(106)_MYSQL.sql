			IMPO_SINGO_NO ASC, 
			JJ_SINCHUNG_DT ASC, 
			JJ_SINCHUNG_SEQ ASC, 
			TONGILSI ASC
		)
)ENGINE=MyISAM DEFAULT CHARSET=euckr;
