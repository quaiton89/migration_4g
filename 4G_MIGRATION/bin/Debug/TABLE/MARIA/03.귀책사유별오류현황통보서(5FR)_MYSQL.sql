	GW2_SINGO_NO VARCHAR(15) NOT NULL,  /* 신고번호 */
	GW2_SINGO_BUHO VARCHAR(5),  /* 신고자부호 */
	GW2_ERROR_SCORE FLOAT, /* 오류점수 */
	PRIMARY KEY (
			GW2_TONGJI_NO ASC, 
			GW2_SINGO_NO ASC
		)
