	DUTY_REQ2 varchar(150),  /* 의무이행요구사항2 */
	DUTY_REQ3 varchar(150),  /* 의무이행요구사항3 */
	CUS_REMARK varchar(1000),  /* 세관기재란 */
	RCV_DATE varchar(14), /* 수신일시 */
	PRIMARY KEY (
			DOC_KEY ASC
		)
